    List<String> getValues() {
        if (defined) {
            List<String> values = new ArrayList<String>();
            for (int i=0;i<vt.getFactType().getSize();i++){
                values.add(((JTextField)panelTE.getComponent(2*i+1)).getText());
               
            }
            return values;
        } else {
            return null;
        }
    }

