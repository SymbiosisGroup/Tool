    void myInit() {

        panelTE.setLayout(new GridLayout(2 * vt.getFactType().getSize() + 1, 1));
        TypeExpression te = vt.getOTE();
        JLabel label = new JLabel(te.constant(0));
        panelTE.add(label);
        for (int i = 0; i < te.getParent().getSize(); i++) {
            JTextField tf = new JTextField();
            panelTE.add(tf);
            label = new JLabel(te.constant(i + 1));
            panelTE.add(label);
        }
    }

