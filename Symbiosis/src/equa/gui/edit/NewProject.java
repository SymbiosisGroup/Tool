    private void jButtonBrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBrowseActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        int returnValue = fileChooser.showSaveDialog(NewProject.this);
        if (returnValue == JFileChooser.APPROVE_OPTION){
            file = fileChooser.getSelectedFile();
            String locationString = file.getAbsolutePath();
            if(!locationString.toLowerCase().endsWith(".sym") ) {
                file = new File(locationString + ".sym");
            }
            jTextFieldLocation.setText(file.getAbsolutePath());
        }
    }//GEN-LAST:event_jButtonBrowseActionPerformed
                                         
