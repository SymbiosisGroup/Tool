    private void jButtonCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreateActionPerformed
        String projectName = jTextFieldProjectName.getText();
        String creatorName = jTextFieldCreatorName.getText();
        String creatorRole = jTextFieldCreatorRole.getText();
        String locationString = jTextFieldCreatorRole.getText();
        if (projectName.isEmpty() || creatorName.isEmpty() || creatorRole.isEmpty() || locationString.isEmpty()) {
            jLabelError.setText("Please fill in all fields");
            jLabelError.setVisible(true);
            return;
        }
        file = new File(locationString);
        project = new Project(projectName, creatorName, creatorRole, true);
        parent.createdNewProject(project, file);
    }//GEN-LAST:event_jButtonCreateActionPerformed

